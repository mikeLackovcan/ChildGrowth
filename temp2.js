async function fetchFromSupabase(email) {
  try {
    const { data: userData } = await supabase.from('users').select('*').eq('email', email).maybeSingle();
    if (userData) {
      localDB.users[email] = {
        role: userData.role,
        parentEmail: userData.parent_email,
        status: userData.status
      };
      
      if (userData.role === 'parent') {
        const { data: kids } = await supabase.from('users').select('*').eq('parent_email', email);
        if (kids) {
          for (const k of kids) {
            localDB.users[k.email] = { role: 'child', parentEmail: k.parent_email, status: k.status };
            if (k.status === 'pending') {
               localDB.requests[k.email] = { childEmail: k.email, parentEmail: email };
            }
            if (k.status === 'approved') {
               const { data: cData } = await supabase.from('child_data').select('state').eq('email', k.email).maybeSingle();
               if (cData) localDB.childData[k.email] = cData.state;
            }
          }
        }
      } else if (userData.role === 'child') {
        const { data: cData } = await supabase.from('child_data').select('state').eq('email', email).maybeSingle();
        if (cData) localDB.childData[email] = cData.state;
      }
    }
    localStorage.setItem('mockDB', JSON.stringify(localDB));
  } catch (err) {
    console.error("Error fetching from Supabase:", err);
  }
}

async function syncToSupabase(db) {
  if (!currentUserEmail || !db.users[currentUserEmail]) return;
  const u = db.users[currentUserEmail];
  
  await supabase.from('users').upsert({
    email: currentUserEmail,
    role: u.role,
    parent_email: u.parentEmail,
    status: u.status
  });

  if (u.role === 'child' && db.childData[currentUserEmail]) {
    await supabase.from('child_data').upsert({
      email: currentUserEmail,
      state: db.childData[currentUserEmail]
    });
  } else if (u.role === 'parent') {
     for (const email of Object.keys(db.users)) {
       const k = db.users[email];
       if (k.role === 'child' && k.parentEmail === currentUserEmail) {
          await supabase.from('users').upsert({
            email: email,
            role: 'child',
            parent_email: currentUserEmail,
            status: k.status
          });
          if (k.status === 'approved' && db.childData[email]) {
             await supabase.from('child_data').upsert({
                email: email,
                state: db.childData[email]
             });
          }
       }
     }
  }
}

