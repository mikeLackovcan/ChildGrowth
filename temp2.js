function todayStr(){
  const d=new Date();
  return d.getFullYear()+'-'+(d.getMonth()+1)+'-'+d.getDate();
}

function streakMultiplier(){
  const s = state.streak;
  if(s >= 30) return 2.0;
  if(s >= 14) return 1.75;
  if(s >= 7)  return 1.5;
  if(s >= 3)  return 1.25;
  return 1.0;
}

function checkNewDay(){
  const today=todayStr();
  if(state.lastDate && state.lastDate!==today){
    const pct=calcPct();
    state.history.unshift({date:state.lastDate,pct,tasks:state.tasks.length, ill:state.isIll});
    if(state.history.length>30) state.history.pop();
    
    if(!state.isIll) {
      const yesterday=new Date(); yesterday.setDate(yesterday.getDate()-1);
      const yStr=yesterday.getFullYear()+'-'+(yesterday.getMonth()+1)+'-'+yesterday.getDate();
      if(state.lastDate===yStr && pct>=50) state.streak++;
      else state.streak=0;
    }
    
    state.tasks = state.tasks.filter(t => t.freq !== 'today');
    state.tasks.forEach(t=>t.done=false);
    state.comfort=0;
    state.isIll=false;
  }
  state.lastDate=today;
  saveState();
}

function calcPct(){
  if(state.isIll) return 100;
  if(!state.tasks.length) return 0;
  return Math.round(state.tasks.filter(t=>t.done).length/state.tasks.length*100);
}

function grantXP(amount) {
  state.xp += amount;
  let lv = state.level;
  let leveledUp = false;
  while(lv < LEVELS.length - 1 && state.xp >= LEVELS[lv].xpNeeded) {
    state.xp -= LEVELS[lv].xpNeeded;
    state.level++;
    lv = state.level;
    leveledUp = true;
  }
  if (leveledUp) {
    launchConfetti();
    showToast(t("levelUp"), '#e8001b');
  }
}

