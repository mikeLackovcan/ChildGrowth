function navigate() {
  document.querySelectorAll('.view').forEach(v => v.classList.remove('active'));
  
  if (!currentUserEmail) {
    document.getElementById('view-login').classList.add('active');
    return;
  }
  
  const db = getDB();
  const user = db.users[currentUserEmail];
  
  if (!user) {
    document.getElementById('view-role').classList.add('active');
    return;
  }
  
  if (user.role === 'parent') {
    renderParentDashboard();
    document.getElementById('view-parent').classList.add('active');
  } else if (user.role === 'child') {
    if (!user.parentEmail) {
      document.getElementById('view-child-onboard').classList.add('active');
    } else if (user.status === 'pending') {
      document.getElementById('view-waiting').classList.add('active');
    } else if (user.status === 'approved') {
      selectedChildEmail = currentUserEmail;
      document.getElementById('view-tracker').classList.add('active');
      initTracker();
    }
  }
}

