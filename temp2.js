// Hold to Claim Logic
function setupHoldBtn(btn, task, cardEl) {
  let timer;
  let progress = 0;
  const fill = btn.querySelector('.hold-fill');
  const text = btn.querySelector('.hold-text');
  
  const start = (e) => {
    e.preventDefault();
    if (task.done) return;
    btn.style.transform = 'scale(0.95)';
    // 600ms hold -> 100% in ~38 ticks of 16ms -> 2.6% per tick
    timer = setInterval(() => {
      progress += 2.6; 
      fill.style.width = progress + '%';
      if(progress >= 100) {
        clearInterval(timer);
        finish(e);
      }
    }, 16);
  };
  
  const end = () => {
    clearInterval(timer);
    if(progress < 100) {
      progress = 0;
      fill.style.width = '0%';
      btn.style.transform = 'scale(1)';
    }
  };
  
  const finish = (e) => {
    btn.style.transform = 'scale(1)';
    btn.style.pointerEvents = 'none';
    fill.style.background = 'var(--red)';
    text.textContent = 'BOOM! 💥';
    
    // Screen shake
    document.body.classList.remove('shake');
    void document.body.offsetWidth;
    document.body.classList.add('shake');
    
    // Visual Loot
    const rect = btn.getBoundingClientRect();
    const clientX = e.type.includes('touch') ? e.changedTouches[0].clientX : e.clientX;
    const clientY = e.type.includes('touch') ? e.changedTouches[0].clientY : e.clientY;
    
    if (!task.isMystery) {
      spawnLoot(clientX || rect.left + rect.width/2, clientY || rect.top + rect.height/2, Math.round(XP[task.diff] * streakMultiplier()));
    }

    // Wait a brief moment to let animations play before completing
    setTimeout(() => completeTask(task, cardEl, clientX || rect.left + rect.width/2, clientY || rect.top + rect.height/2), 500);
  };

  btn.addEventListener('mousedown', start);
  btn.addEventListener('mouseup', end);
  btn.addEventListener('mouseleave', end);
  btn.addEventListener('touchstart', start, {passive: false});
  btn.addEventListener('touchend', end);
}

function completeTask(task, cardEl, clientX, clientY) {
  task.done = true;
  let xpGained = Math.round(XP[task.diff] * streakMultiplier());
  
  if (task.isMystery) {
    const multipliers = [1.5, 2, 3, 5];
    const multi = multipliers[Math.floor(Math.random() * multipliers.length)];
    xpGained = Math.round(xpGained * multi);
    
    const toast = document.createElement('div');
    toast.className = 'mystery-toast';
    toast.innerHTML = `<div class="mystery-toast-title">Jackpot!</div><div class="mystery-toast-xp">+${xpGained} XP</div>`;
    document.body.appendChild(toast);
    setTimeout(() => toast.remove(), 2600);
    
    if(clientX && clientY) {
      spawnLoot(clientX, clientY, xpGained);
      spawnLoot(clientX - 50, clientY, xpGained);
      spawnLoot(clientX + 50, clientY, xpGained);
    }
  }
  
  grantXP(xpGained);
  
  const now = Date.now();
  if(now - lastCheckMs < 4000) comboCount++; else comboCount = 1;
  lastCheckMs = now;
  
  if(comboCount >= 2){
    const bonus = comboCount >= 4 ? 15 : comboCount === 3 ? 10 : 5;
    grantXP(bonus);
    showComboToast(comboCount, bonus, cardEl);
  }
  
  if(state.tasks.filter(x=>x.done).length === 1) showToast(t("firstQuest"),'#00e676');
  
  if(state.tasks.every(x => x.done)){
    setTimeout(() => {
      renderWheel();
      document.getElementById('wheel-modal').classList.add('show');
    }, 800);
  }
  renderTracker();
}

function undoTask(task) {
  task.done = false;
  comboCount = 0; 
  const xpLost = Math.round(XP[task.diff] * streakMultiplier());
  state.xp -= xpLost;
  if (state.xp < 0 && state.level > 0) {
     state.level--;
     state.xp += LEVELS[state.level].xpNeeded;
  } else if (state.xp < 0) {
     state.xp = 0;
  }
  renderTracker();
}

function renderTasks() {
  const el = document.getElementById('task-list');
  el.innerHTML = '';
  
  if(!state.tasks.length) {
    el.innerHTML = '<div style="text-align:center; padding: 20px; color: var(--text3); font-weight: 800;">No quests assigned! Take a break.</div>';
    return;
  }

  // 1. Identify the Epic Boss (the incomplete task with the highest XP)
  let epicTask = null;
  const incompleteTasks = state.tasks.filter(t => !t.done);
  if (incompleteTasks.length > 0) {
    const diffScores = { 'hard': 3, 'med': 2, 'easy': 1 };
    epicTask = incompleteTasks.reduce((prev, curr) => (diffScores[curr.diff] > diffScores[prev.diff] ? curr : prev));
  }

  // Render Epic Boss if exists
  if (epicTask) {
    const epicCard = document.createElement('div');
    epicCard.className = 'epic-card task-card';
    const freqIcon = epicTask.freq === 'today' ? '🕒 ' : '';
    epicCard.innerHTML = `
      <div class="epic-title">🌟 EPIC DAILY BOSS</div>
      <div style="font-size: 16px; font-weight: 800; color: #fff; margin-bottom: 6px;">${freqIcon}${epicTask.name}</div>
      <div class="hold-btn" id="hold-${epicTask.id}">
        <div class="hold-fill"></div>
        <div class="hold-text">HOLD TO CLAIM (+${Math.round(XP[epicTask.diff] * streakMultiplier())} XP)</div>
      </div>
    `;
    el.appendChild(epicCard);
    setupHoldBtn(epicCard.querySelector('.hold-btn'), epicTask, epicCard);
  }

  // Render rest of the tasks
  state.tasks.forEach((t) => {
    if (t === epicTask) return; // already rendered as epic
    
    const card = document.createElement('div');
    card.className = 'task-card' + (t.done ? ' done' : '');
    const freqIcon = t.freq === 'today' ? '🕒 ' : '';
    
    if (t.done) {
      card.innerHTML = `
        <div class="stamp">CLEARED</div>
        <div style="opacity: 0.4; width: 100%; display: flex; align-items: center; gap: 12px;">
          <div class="check on"></div>
          <span class="task-name" style="text-decoration: line-through;">${freqIcon}${t.name}</span>
          <button class="delete-btn undo-btn" title="Undo" style="opacity:1; font-size: 11px; font-weight: 900; background: var(--surface2); border: 1px solid var(--border); border-radius: 4px; padding: 4px 8px;">UNDO</button>
        </div>
      `;
      card.querySelector('.undo-btn').addEventListener('click', (e) => {
        e.stopPropagation(); undoTask(t);
      });
    } else {
      card.innerHTML = `
        <div style="width: 100%; display: flex; flex-direction: column; gap: 8px;">
          <div style="font-size: 14px; font-weight: 700; color: #fff;">${freqIcon}${t.name}</div>
          <div style="display:flex; gap: 10px; align-items: center;">
            <span class="diff-tag diff-${t.diff}">${t.diff==='med'?'Medium':t.diff==='easy'?'Easy':'Hard'}</span>
            <div class="hold-btn ${t.isMystery ? 'mystery-btn' : ''}" style="height: 36px; flex: 1; font-size: 12px;" id="hold-${t.id}">
              <div class="hold-fill" style="background: var(--blue);"></div>
              <div class="hold-text">${t.isMystery ? 'HOLD TO OPEN MYSTERY 🎁' : `${t('holdBase')} (+${Math.round(XP[t.diff] * streakMultiplier())} XP)`}</div>
            </div>
            ${getDB().users[currentUserEmail].role === 'parent' ? `<button class="delete-btn remove-btn" title="Remove" style="opacity:1">×</button>` : ''}
          </div>
        </div>
      `;
      setupHoldBtn(card.querySelector('.hold-btn'), t, card);
      
      if (getDB().users[currentUserEmail].role === 'parent') {
        card.querySelector('.remove-btn').addEventListener('click', (e) => {
          e.stopPropagation();
          state.tasks = state.tasks.filter(x => x.id !== t.id);
          renderTracker();
        });
      }
    }
    el.appendChild(card);
  });
}

